# ─── Self-contained primitives (integer-only, mirrors suite) ──────────────────

def rol(x: int, r: int, n: int) -> int:
    r %= n
    return ((x << r) | (x >> (n - r))) & ((1 << n) - 1)

def fscx(A: int, B: int, n: int) -> int:
    return (A ^ B ^ rol(A, 1, n) ^ rol(B, 1, n) ^ rol(A, n-1, n) ^ rol(B, n-1, n)) \
           & ((1 << n) - 1)

def delta_v2(K: int, n: int) -> int:
    """delta(K) = ROL(K * ceil(K/2) mod 2^n, n/4)."""
    return rol((K * ((K + 1) >> 1)) & ((1 << n) - 1), n >> 2, n)

def m_step(X: int, n: int) -> int:
    """One application of M = I XOR ROL XOR ROR."""
    return X ^ rol(X, 1, n) ^ rol(X, n - 1, n)

def m_inv(Y: int, n: int) -> int:
    """M^{-1}(Y) = M^{n/2 - 1}(Y).  M has order n/2, so M^{-1} = M^{n/2-1}."""
    X = Y
    for _ in range(n // 2 - 1):
        X = m_step(X, n)
    return X

def pi_K(A: int, K: int, n: int) -> int:
    """pi_K(A) = (M(A XOR K) + delta(K)) mod 2^n."""
    return (fscx(A, K, n) + delta_v2(K, n)) & ((1 << n) - 1)

def pi_K_inv(Y: int, K: int, n: int) -> int:
    """pi_K^{-1}(Y) = K XOR M^{-1}((Y - delta(K)) mod 2^n)."""
    mask = (1 << n) - 1
    return K ^ m_inv((Y - delta_v2(K, n)) & mask, n)

def orbit_brent(G: int, K: int, n: int, cap: int) -> tuple:
    """Brent's cycle detection for orbit of G under pi_K.
    Returns (length, capped) where capped=True if cap was reached before cycle closed."""
    power = 1; lam = 1
    tortoise = G
    hare = pi_K(G, K, n)
    steps = 1
    while hare != tortoise:
        if power == lam:
            tortoise = hare; power <<= 1; lam = 0
        hare = pi_K(hare, K, n)
        lam += 1; steps += 1
        if steps > cap:
            return lam, True
    return lam, False


# ─── §1: Exhaustive cycle structure at n = 4, 6, 8 ───────────────────────────

def _cycle_decomp(K: int, n: int) -> list:
    """Sorted list of all cycle lengths in the permutation pi_K on {0,...,2^n-1}."""
    N = 1 << n
    visited = bytearray(N)
    cycles = []
    for start in range(N):
        if visited[start]:
            continue
        length = 0
        x = start
        while not visited[x]:
            visited[x] = 1
            x = pi_K(x, K, n)
            length += 1
        cycles.append(length)
    cycles.sort()
    return cycles


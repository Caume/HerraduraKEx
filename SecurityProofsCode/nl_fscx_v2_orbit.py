    print("§1 — Exhaustive Cycle Structure at n = 4, 6, 8")
    print(SEP)
    print("  For each K in {0,1}^n: full cycle decomposition of pi_K.")
    print("  K=0 is degenerate (pi_0 = M, a GF(2)-linear map of order n/2).")
    print()

    for n in [4, 6, 8]:
        N = 1 << n
        print(f"  n={n}  (state space {N}, key space {N} — exhaustive)")
        print(f"  {SEP2[:60]}")

        # K=0 special case first
        c0 = _cycle_decomp(0, n)
        print(f"  K=0 (degenerate linear): {len(c0)} cycles, lengths = {c0[:16]}")
        print(f"    max cycle = {max(c0)}, min cycle = {min(c0)}  (expected ≤ n/2={n//2})")
        print()

        # Statistics over all K

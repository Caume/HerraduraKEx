# ─── §2: Sampled orbit-length distribution at n = 8, 12, 16 ─────────────────

def section2():
    print(SEP)
    print("§2 — Sampled Orbit-Length Distribution at n = 8, 12, 16")
    print(SEP)
    print("  Random (K, G) pairs, K != 0.  Orbit length via Brent's (exact, no cap).")
    print()

    for n, n_samples in [(8, 2000), (12, 1000), (16, 400)]:
        mask = (1 << n) - 1
        cap  = 1 << n          # exact — no orbit can exceed 2^n
        lengths = []
        for _ in range(n_samples):
            K = random.randint(1, mask)
            G = random.randint(0, mask)
            L, _ = orbit_brent(G, K, n, cap)
            lengths.append(L)
        lengths.sort()

        mn   = lengths[0]
        mx   = lengths[-1]
        mean = sum(lengths) / len(lengths)
        med  = lengths[len(lengths) // 2]
        p10  = lengths[int(0.10 * len(lengths))]
        p90  = lengths[int(0.90 * len(lengths))]

        def lg(v): return math.log2(v) if v > 0 else 0

        print(f"  n={n},  {n_samples} samples:")
        print(f"    min={mn}  p10={p10}  median={med}  mean={mean:.0f}  p90={p90}  max={mx}")
        print(f"    (log2: min={lg(mn):.2f}  median={lg(med):.2f}  mean={lg(mean):.2f}  max={lg(mx):.2f})")

        # Distribution by power-of-two bucket
        buckets: dict = {}
        for L in lengths:
            b = max(0, int(lg(L)))
            buckets[b] = buckets.get(b, 0) + 1
        print(f"    Distribution by power-of-2 bucket (each █ = {n_samples//50}):")
        for b in sorted(buckets):
            bar = "█" * max(1, buckets[b] * 50 // n_samples) if buckets[b] else ""
            print(f"      [2^{b:<2}..2^{b+1}) {buckets[b]:>5}  {bar}")

        # Fraction with orbit length > N/4, N/2, N
        N = 1 << n
        above_q = sum(1 for L in lengths if L >  N // 4) / len(lengths)
        above_h = sum(1 for L in lengths if L >  N // 2) / len(lengths)
        above_f = sum(1 for L in lengths if L == N)       / len(lengths)
        print(f"    orbit > N/4: {100*above_q:.1f}%   orbit > N/2: {100*above_h:.1f}%   orbit = N (full): {100*above_f:.1f}%")
        print()


# ─── §3: Non-commutativity verification ──────────────────────────────────────

def section3():
    print(SEP)
    print("§3 — Non-Commutativity Verification (Theorem 15 Empirical Check)")
    print(SEP)
    print("  Theorem 15: {pi_K} is a non-abelian family — pi_{K1} o pi_{K2} != pi_{K2} o pi_{K1}")
    print("  for generic K1 != K2.")
    print("  Test: for random (K1 != K2, A), check pi_{K1}(pi_{K2}(A)) != pi_{K2}(pi_{K1}(A)).")
    print()

    for n in [8, 16, 32]:
        mask = (1 << n) - 1
        trials = 40_000
        comm = 0      # compositions agree (commuting point)
        non_comm = 0  # compositions differ
        for _ in range(trials):
            K1 = random.randint(1, mask)
            K2 = random.randint(1, mask)
            while K2 == K1:
                K2 = random.randint(1, mask)
            A = random.randint(0, mask)
            lhs = pi_K(pi_K(A, K1, n), K2, n)   # pi_{K2} o pi_{K1}
            rhs = pi_K(pi_K(A, K2, n), K1, n)   # pi_{K1} o pi_{K2}
            if lhs == rhs:
                comm += 1
            else:
                non_comm += 1
        rate = 100 * non_comm / trials
        print(f"  n={n:>2}:  {non_comm}/{trials} triples non-commuting  ({rate:.2f}%)")
        if comm:
            print(f"         {comm} commuting points found (may be coincidental point agreements,")
            print(f"         not evidence that the pair commutes as a permutation)")
    print()
    print("  Commuting point != commuting permutation: two non-commuting permutations can")
    print("  agree on isolated points by coincidence.  For Theorem 15, the relevant")
    print("  criterion is whether pi_{K1} o pi_{K2} = pi_{K2} o pi_{K1} as functions.")
    print()

    # For n=8: check full permutation commutativity (all 256 points must agree)
    n = 8; N = 1 << n; mask = N - 1
    trials_full = 1000
    full_comm = 0
    for _ in range(trials_full):
        K1 = random.randint(1, mask)
        K2 = random.randint(1, mask)
        while K2 == K1:
            K2 = random.randint(1, mask)
        if all(pi_K(pi_K(A, K1, n), K2, n) == pi_K(pi_K(A, K2, n), K1, n)
               for A in range(N)):
            full_comm += 1
    print(f"  n=8 full-permutation commutativity: {full_comm}/{trials_full} pairs commute  ({100*full_comm/trials_full:.1f}%)")
    print(f"  (Pairs where pi_{{K1}} o pi_{{K2}} == pi_{{K2}} o pi_{{K1}} for ALL A in {{0..255}})")



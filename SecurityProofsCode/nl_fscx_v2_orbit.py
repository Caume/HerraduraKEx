# ─── §5: CSP attack feasibility at n = 6, 8 ──────────────────────────────────

def section5():
    print(SEP)
    print("§5 — CSP Attack Feasibility at n = 6, 8 (Exhaustive Brute-Force)")
    print(SEP)
    print("  Construction: C = pi_{K1}(pi_{K2}(pi_{K1}^{-1}(G)))")
    print("  Attack: given (G, K2, C), find all K1 in {1,...,2^n-1} satisfying equation.")
    print("  K1=0 excluded (degenerate).")
    print()

    for n in [6, 8]:
        N = 1 << n
        G = N // 3             # arbitrary fixed public base

        n_trials = 300 if n == 6 else 200
        sol_counts = []
        unique_trials = 0

        for _ in range(n_trials):
            K1 = random.randint(1, N - 1)
            K2 = random.randint(1, N - 1)
            while K2 == K1:
                K2 = random.randint(1, N - 1)

            # Compute C = pi_{K1}(pi_{K2}(pi_{K1}^{-1}(G)))
            inner  = pi_K_inv(G, K1, n)
            middle = pi_K(inner, K2, n)
            C      = pi_K(middle, K1, n)

            # Brute-force: all candidate K1' values
            hits = 0
            for K1p in range(1, N):
                inner_p  = pi_K_inv(G, K1p, n)
                middle_p = pi_K(inner_p, K2, n)
                Cp       = pi_K(middle_p, K1p, n)
                if Cp == C:
                    hits += 1
            sol_counts.append(hits)
            if hits == 1:
                unique_trials += 1

        zero  = sol_counts.count(0)
        one   = sol_counts.count(1)
        multi = sum(1 for s in sol_counts if s > 1)
        avg   = sum(sol_counts) / len(sol_counts)
        max_s = max(sol_counts)

        print(f"  n={n}  (key space {N-1}, {n_trials} random (K1 != K2) trials, G={G}):")
        print(f"    Solutions per trial: avg={avg:.2f}  max={max_s}")
        print(f"    0 solutions (bug): {zero}   1 solution (unique): {one}   >1 solutions: {multi}")
        print(f"    Unique-solution rate: {100*unique_trials/n_trials:.1f}%")
        if multi > 0:
            extras = [s for s in sol_counts if s > 1]
            print(f"    Multi-solution counts (sample): {sorted(extras)[:10]}")
        print()

    print("  A unique solution means the CSP has effectively one answer in this space.")
    print("  Multiple solutions occur when distinct K1 values produce the same (G,K2,C)")
    print("  triple — an algebraic collision in the conjugation map.")
    print("  At production bit-sizes (n=256), brute-force is infeasible regardless.")


